package Test2_V5_ChainOfResposabilities;

public class Token {
    String nume;
    String parola;

    public Token(String nume, String par) {
        this.nume = nume;
        this.parola = par;
    }

    public String getNume() {
        return this.nume;
    }

    public String getParola() {
        return parola;
    }
}
